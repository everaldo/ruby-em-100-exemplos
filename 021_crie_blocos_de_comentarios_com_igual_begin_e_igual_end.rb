# 021 - Crie blocos de comentários com =begin e =end
#
# Já vimos que linhas que começam com # são comentários
# e totalmente ignoradas.
#
# Se uma linha tiver um comando e depois #, o restante da linha
# é ignorada como comentário.
#
# Ruby também possui um recurso de comentários em blocos, semelhante
# ao /* e */ da linguagem C.
#
# Utiliza-se =begin e =end no começo de uma linha.
#
# Esse recurso não é muito utilizado por conta dos editores de texto
# modernos, que rapidamente comentam e descomentam blocos utilizando
# apenas o caractere #
#



